TAG_TO_ADD = "discount_15"


async def main():
    """Add discount_15 tag to products."""
    print(f"📝 Adding '{TAG_TO_ADD}' tag to {len(PRODUCT_IDS)} products...")

    updated_count = 0
    not_found_count = 0
    already_tagged_count = 0

    for product_id in PRODUCT_IDS:
        try:
            # Find the product
            product = await db.product_normalized.find_one({"_id": product_id})

            if not product:
                print(f"  ❌ Not found: {product_id}")
                not_found_count += 1
                continue

            # Get current tags
            current_tags = product.get("tags", []) or []

            # Check if tag already exists
            if TAG_TO_ADD in current_tags:
                print(f"  ⏭️  Already tagged: {product_id}")
                already_tagged_count += 1
                continue

            # Add the tag
            new_tags = current_tags + [TAG_TO_ADD]

            await db.product_normalized.update_one(
                {"_id": product_id},
                {"$set": {"tags": new_tags}},
            )

            print(f"  ✅ Tagged: {product_id}")
            updated_count += 1

        except Exception as e:
            print(f"  ⚠️  Error processing {product_id}: {e}")

    # Summary
    print("\n📊 Summary:")
    print(f"  ✅ Updated: {updated_count}")
    print(f"  ⏭️  Already tagged: {already_tagged_count}")
    print(f"  ❌ Not found: {not_found_count}")
    print(f"  📝 Total processed: {len(PRODUCT_IDS)}")

    close_mongo_client()


if __name__ == "__main__":
    asyncio.run(main())
